public class SwitchCase{
    public static void main(String args[]) {
        int opcao = 0;

        switch (opcao) {
            case 1:
                System.out.println("Opcao 1");
                break;
            case 2:
                System.out.println("Opcao 2");
                break;
            case 3:
                System.out.println("Opcao 3");
                break;
            default:
                System.out.println("Opcao Inexistente.");
                break;
        }
    }
}
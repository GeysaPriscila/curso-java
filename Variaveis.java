/**
 * Variáveis
 */
public class Variaveis {
    public static void main(String args[]){
        /*
         * Tipos Variáveis:
         * Tipos inteiros byte=127, short=42, int=3, long=22L
         * Tipos reais float=12.7f, double=42.
         * Tipos caractere char='1'
         * Tipo boleano boolean=true/false
         * Cadeia de caracteres String="letra"
         */
        char letra = 'A';
        System.out.println(letra);
        int ano = 2020;
        System.out.println(ano);
        float peso = 63.2f;
        System.out.println(peso);
        boolean verifica = true;
        System.out.println(verifica);
        String nome = "Geysa";
        System.out.println(nome);
    }
}
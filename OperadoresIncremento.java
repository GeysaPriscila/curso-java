/**
 * Operadores de Incremento
 * a++ e a-- => primeiro faz o processamento para depois realizar o incremento.
 * ++a e --a => primeiro faz o incremento para depoisrealizar o processamento.
 */
public class OperadoresIncremento {
    public static void main(String args[]) {
        int num = 10;
        //num = num + 1
        num++;

        System.out.println(num);
    }
}